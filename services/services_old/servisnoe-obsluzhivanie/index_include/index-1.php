<h2>Сервисное обслуживание системы вентиляции</h2>

<p>Техническое обслуживание систем вентиляции необходимо проводить планово и своевременно. Это гарант и неотъемлемая часть правильной эксплуатации систем вентиляции. Если не осуществлять профилактическое обслуживание, вся система вентиляции может работать некорректно или вообще перестанет функционировать. Сервисное обслуживание продлевает срок службы вентиляционных агрегатов и поддерживает качество воздуха на необходимом уровне.</p>
<p>Сервис вентиляции включает в себя следующие операции:</p>
<ul>
	<li>Внешний осмотр вентиляционных агрегатов.</li>
	<li>Проверка прочности крепления установок, вентиляторов и вентиляционных компонентов.</li>
	<li>Диагностика электрических соединений, замер параметров.</li>
	<li>Проверка работы элементов автоматики.</li>
	<li>Осмотр воздуховодов.</li>
	<li>Очистка диффузоров и решеток от пыли и грязи.</li>
	<li>Замена воздушных фильтров.</li>
	<li>Замер температуры и скорости воздуха.</li>
	<li>Приведение параметров воздуха к проектным показателям.</li>
</ul>
<p> Сервисное обслуживание в жилых и помещениях с небольшим количеством людей производится 1-2 раза в год.</p>
<p>	Чистка систем вентиляции в кафе, ресторанах, столовых производится минимум 4 раза в год.</p>
<p>	Обслуживание систем вентиляции промышленного назначения производится 6-12 раз в год.</p>

<p>Стоимость сервисного обслуживания систем вентиляции рассчитывается индивидуально.</p>
