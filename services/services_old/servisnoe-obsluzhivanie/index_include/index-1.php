<h2>Сервисное обслуживание кондиционеров</h2>

<p>В воздухе вокруг нас присутствует пыль, бактерии, различные неприятные запахи. Этот воздух проходит через теплообменники внутреннего и наружного блоков. И если кондиционер не чистить, эта грязь скапливается на фильтрах, теплообменниках, вентиляторах и в дренажных поддонах. Засоряются дренажные трубы и конденсат не уходит.</p>
<p>	Если кондиционер стал плохо охлаждать или вообще перестал, обмерзает, шумит, из него капает вода и появился неприятный запах, то пора делать сервисное обслуживание. Плановое сервисное обслуживание кондиционера – это работы, которые периодически выполняются специалистами для предотвращения серьезных поломок.</p>
<p>Обычно, в помещениях с небольшой загрязненностью (квартиры, жилые дома), сервис кондиционера проводится 1-2 раз в год до и после сезона эксплуатации.</p>
<p>	В помещениях с большим количеством людей (офисы, магазины, кафе, рестораны) и постоянной эксплуатацией кондиционеров, сервис производится 2-4 раза в год или раз в квартал.</p>
<p>	В помещениях специального и промышленного назначения (производственные цеха, серверные и пр.) рекомендуется ежемесячное обслуживание.</p>
