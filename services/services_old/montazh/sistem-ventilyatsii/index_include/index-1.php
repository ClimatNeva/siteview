<h2>Установка вентиляционных систем</h2>
<p>
 <b>Вентиляционная система состоит из нескольких компонентов:</b>
</p>
<ul>
	<li> Вентиляционное оборудование;</li>
	<li>
	Воздуховоды и фасонные элементы;</li>
	<li>
	Воздухораспределители и вентиляционные решетки.</li>
</ul>
<p>
 <b>Монтажные работы проводятся в несколько этапов:</b>
</p>
<ol>
	<li>
	Прокладка воздуховодов;</li>
	<li> Монтаж вентиляционного оборудования и элементов;</li>
	<li> Установка воздухораспределительных устройств;</li>
	<li> Пуско-наладочные работы. Сдача в эксплуатацию.</li>
</ol>
<p>
	 Прокладка воздуховодов производится на этапе ремонта или реконструкции, до чистовой отделки. Чаще всего потом они скрыты потолком.
</p>
<p>
	 По факту поставки вентиляционного оборудования он монтируется и по возможности осуществляется первичный запуск системы. Он необходим для начальной отладки системы по расходам и шуму. Пока не смонтирован потолок, можно еще изменить трассировку, добавить шумоглушители и настроить воздушные клапаны.
</p>
<p>
	 Если оборудования нет в наличии, то срок поставки его составляет 3-4 недели. Если есть в наличии, то оно тоже монтируется в первом этапе.
</p>
<p>
	 По чистовой отделке производится установка диффузоров и вентиляционных решеток.
</p>
<p>
	 Завершающий этап - пусконаладочные работы. К этому этапу к оборудованию должны быть подведены по постоянной схеме электроснабжение и теплоснабжение. Настраивается автоматика. Проводятся испытания в различных режимах работы и замер параметров. Итоговая цель пуско-наладочных работ – достижение проектных показателей системы.
</p>
