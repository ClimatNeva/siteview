<p>
	 Вентиляционная установка может быть моноблочной или наборной, напольной или подвесной, внутреннего или уличного исполнения, воздуховоды из оцинковки, нержавейки или фольгированный гибкие. Общеобменная приточная и вытяжная система, местная вытяжная и приточная вентиляция, бесканальная и канальная вентиляция.
</p>
<p>
	 Это необходимо определять на стадии проектирования или составления техзадания на проектирование. Но от выбора того или иного решения зависит и стоимость монтажных работ.
</p>
<p>
	 Стоимость монтажа систем вентиляции и кондиционирования зависит от мощности (производительности) оборудования и комплектности систем.
</p>
<p>
	 Так, стоимость монтажа готвой вентиляционной установки ниже, чем монтаж каждого из элементов вентиляционной системы (вентилятор, фильтр, нагреватель, шумоглушитель и т.д.) в отдельности. Итоговая цена зависит от длины воздуховодов, количества клапанов и решеток.
